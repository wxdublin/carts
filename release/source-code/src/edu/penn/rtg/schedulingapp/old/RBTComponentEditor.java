	/**
	 * Action Listener
	 * @param e
	 *            action event
	 */

	public void actionPerformed(ActionEvent e) {
		if (!nameBox.getText().equalsIgnoreCase("")
				&& possibleParents.getSelectedIndex() != -1) {
			RBTComponent parent = (RBTComponent) possibleParents
					.getSelectedValue();
			RBTComponent child = new RBTComponent(nameBox.getText(), parent);

			child.setAlgorithm((String) algBox.getSelectedItem());
			//child.setSubUnit(new TaskList());

			try {
				double min, max;
				min = Double.parseDouble(minPeriod.getText());
				max = Double.parseDouble(maxPeriod.getText());

				if (min > max || min < 1 || max < 1) {
					new PopupException(
							"Please use non-negative numbers for your mimimum and maximum periods, and make sure that your minimum is less than your maximum.");
					return;
				}
				child.setMinPeriod(min);
				child.setMaxPeriod(max);
			} catch (Exception ex) {
				new PopupException(
						"Please use numbers for your mimimum and maximum periods.");
				return;
			}

			i.addComponent(child, parent);
			i.repaint();

			this.setVisible(false);
		} else {
			new PopupException("Please make a name for this component");
			return;
		}
	}

